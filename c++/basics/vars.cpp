#include <iostream>

using namespace std;

int main()
{
    int age = 35;
    string name = "John";

    cout << "There once was a name named " << name << endl;
    cout << "He was " << age << " years old" << endl;

    name = "Mike";

    cout << "He liked the name " << name << endl;
    cout << "But did not like being " << age << endl;
}